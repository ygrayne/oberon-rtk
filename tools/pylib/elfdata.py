def instr_size(hex_instr):
    return 2 if len(hex_instr) <= 5 else 4
def is_data_line(rest):
    return DATA_ANNOTATION_RE.search(rest) is not None
def parse_push_regs(mnemonic_args):
    m = PUSH_REGS_RE.search(mnemonic_args)
    if not m:
        return None
    regs = []
    for part in m.group(1).split(','):
        part = part.strip().lower()
        if part == 'lr':
            regs.append(14)
        elif part == 'sp':
            regs.append(13)
        elif part == 'pc':
            regs.append(15)
        elif part.startswith('r'):
            try:
                regs.append(int(part[1:]))
            except ValueError:
                pass
    regs.sort()
    return regs if regs else None
def parse_listing(filepath, source_lines=False):
    lines = filepath.read_text(errors='replace').splitlines()
    module_src = None
    module_name = None
    try:
        tokens = tokenize_alst(filepath)
        for ti, tok in enumerate(tokens):
            if tok.text == 'MODULE' and ti + 1 < len(tokens):
                module_name = tokens[ti + 1].text
                break
        parser = OberonParser(tokens)
        module_src = parser.parse_module()
        if module_src is not None:
            module_src.consts = dict(parser.const_defs)
    except Exception:
        pass
    if module_name is None:
        return None
    asm_lines = []
    for i, line in enumerate(lines):
        m = ASM_LINE_RE.match(line)
        if m:
            asm_lines.append((
                i + 1,
                int(m.group(1)),
                int(m.group(2), 16),
                m.group(3),
                m.group(4).strip(),
            ))
    if not asm_lines:
        return None
    addr_to_idx = {abs_addr: idx for idx, (_, _, abs_addr, _, _) in enumerate(asm_lines)}
    procedures = []
    if module_src is not None:
        for proc_src in module_src.procedures:
            decl_line = proc_src.decl_line
            if decl_line <= 0:
                continue
            for k in range(decl_line - 1, len(lines)):
                am = ASM_LINE_RE.match(lines[k])
                if am:
                    addr = int(am.group(2), 16)
                    procedures.append((proc_src.name, addr))
                    break
    init_addr = None
    last_pop_idx = None
    for j in range(len(asm_lines) - 1, -1, -1):
        if POP_PC_RE.search(asm_lines[j][4]):
            last_pop_idx = j
            break
    if last_pop_idx is not None:
        for j in range(last_pop_idx - 1, -1, -1):
            if PUSH_LR_RE.search(asm_lines[j][4]):
                init_addr = asm_lines[j][2]
                break
    proc_addrs = {addr for _, addr in procedures}
    if init_addr is not None and init_addr not in proc_addrs:
        procedures.append(('..init', init_addr))
    procedures.sort(key=lambda p: p[1])
    last_asm = asm_lines[-1]
    file_end_addr = last_asm[2] + instr_size(last_asm[3])
    proc_symbols = []
    for i, (name, addr) in enumerate(procedures):
        full_name = (f'{module_name}..init' if name == '..init'
                     else f'{module_name}.{name}')
        size = (procedures[i + 1][1] if i + 1 < len(procedures)
                else file_end_addr) - addr
        proc_symbols.append(ProcSymbol(name=full_name, address=addr, size=size))
    frame_infos = []
    for proc in proc_symbols:
        idx = addr_to_idx.get(proc.address)
        if idx is None:
            continue
        proc_end = proc.address + proc.size
        while idx < len(asm_lines) and asm_lines[idx][2] < proc_end:
            _, _, _, hex_instr, rest = asm_lines[idx]
            saved_regs = parse_push_regs(rest)
            if saved_regs is not None:
                break
            idx += 1
        else:
            continue
        if saved_regs is None:
            continue
        push_sz = instr_size(hex_instr)
        sp_adj = 0
        sub_sz = 0
        if idx + 1 < len(asm_lines):
            _, _, _, next_hex, next_rest = asm_lines[idx + 1]
            sm = SUB_SP_RE.search(next_rest)
            if sm:
                sp_adj = int(sm.group(1))
                sub_sz = instr_size(next_hex)
        frame_infos.append(FrameInfo(
            address=proc.address,
            size=proc.size,
            push_size=push_sz,
            saved_regs=saved_regs,
            sp_adjust=sp_adj,
            sub_sp_size=sub_sz,
        ))
    line_entries = []
    if source_lines:
        current_source_line = 1
        for i, line in enumerate(lines):
            if ASM_LINE_RE.match(line):
                m = ASM_LINE_RE.match(line)
                if m and not is_data_line(m.group(4).strip()):
                    abs_addr = int(m.group(2), 16)
                    line_entries.append(LineEntry(address=abs_addr, line=current_source_line))
            elif line.strip():
                current_source_line = i + 1
    else:
        for line_no, rel_off, abs_addr, hex_inst, rest in asm_lines:
            if not is_data_line(rest):
                line_entries.append(LineEntry(address=abs_addr, line=line_no))
    low_pc  = asm_lines[0][2]
    high_pc = file_end_addr
    type_sizes = {}
    if module_src is not None:
        for type_name, decl_line in module_src.record_decl_lines.items():
            record_depth = 0
            found_record = False
            for k in range(decl_line - 1, len(lines)):
                line = lines[k]
                if ASM_LINE_RE.match(line):
                    if found_record and record_depth <= 0:
                        tm = TYPE_ANNOT_RE.search(line)
                        if tm:
                            size = int(tm.group(1))
                            if size > 0:
                                type_sizes[type_name] = size
                    break
                else:
                    stripped = line.strip()
                    for _ in re.findall(r'\bRECORD\b', stripped):
                        record_depth += 1
                        found_record = True
                    for _ in re.findall(r'\bEND\b', stripped):
                        record_depth -= 1
        module_src.type_sizes = type_sizes
    return ModuleDebug(
        name=module_name,
        filename=filepath.name,
        procedures=proc_symbols,
        line_entries=line_entries,
        frame_infos=frame_infos,
        low_pc=low_pc,
        high_pc=high_pc,
        module_src=module_src,
    )
