def type_size_ext(typeref, type_sizes=None):
    if typeref is None:
        return 4
    if typeref.is_proc_type:
        return 4
    if typeref.pointer_to is not None:
        return 4
    if typeref.fixed_dim > 0 and typeref.elem is not None:
        return typeref.fixed_dim * type_size_ext(typeref.elem, type_sizes)
    info = OBERON_BASE_TYPES.get(typeref.name)
    if info:
        return info[0]
    if type_sizes and typeref.name in type_sizes:
        return type_sizes[typeref.name]
    if typeref.fields is not None:
        _, total = compute_local_addresses(typeref.fields, type_sizes)
        return total
    return 4
def type_align_ext(typeref, type_sizes=None):
    if typeref is None:
        return 4
    if typeref.fixed_dim > 0 or typeref.open_array:
        return 4
    info = OBERON_BASE_TYPES.get(typeref.name)
    if info:
        return 1 if info[0] == 1 else 4
    return 4
def type_alloc_size(typeref, type_sizes=None):
    sz = type_size_ext(typeref, type_sizes)
    if typeref is not None and (typeref.fixed_dim > 0 or typeref.open_array):
        return (sz + 3) & ~3
    return sz
def _name_anonymous_records(typeref, prefix):
    if typeref.fields is None:
        return
    for var in typeref.fields:
        tr = var.typeref
        if tr is not None and tr.name == 'RECORD' and tr.fields is not None:
            field_name = var.names[0] if var.names else 'anon'
            synthetic = f'{prefix}__{field_name}'
            tr.name = synthetic
            _name_anonymous_records(tr, synthetic)
def typeref_key(typeref):
    if typeref.is_proc_type:
        return 'PROCEDURE'
    if typeref.fixed_dim > 0:
        if typeref.elem and typeref.elem.fixed_dim > 0:
            elem_key = typeref_key(typeref.elem)
            return f'ARRAY{typeref.fixed_dim}_{elem_key}'
        elem = typeref.elem.name if typeref.elem else typeref.name
        return f'ARRAY{typeref.fixed_dim}_{elem}'
    if typeref.open_array:
        elem = typeref.elem.name if typeref.elem else typeref.name
        return f'PTR_{elem}'
    if typeref.pointer_to is not None:
        target_name = typeref_key(typeref.pointer_to)
        return f'PTRTO_{target_name}'
    if typeref.scope:
        return f'{typeref.scope}::{typeref.name}'
    return typeref.name
class TK(Enum):
    IDENT      = auto()
    INTEGER    = auto()
    COMMA      = auto()
    COLON      = auto()
    SEMI       = auto()
    DOT        = auto()
    DOTDOT     = auto()
    STAR       = auto()
    EQ         = auto()
    LPAREN     = auto()
    RPAREN     = auto()
    LBRACKET   = auto()
    RBRACKET   = auto()
    EOF        = auto()
@dataclass
class Token:
    kind: TK
    text: str
    line: int
_SECTION_KEYWORDS = frozenset([
    'VAR', 'TYPE', 'CONST', 'PROCEDURE', 'BEGIN', 'END', 'MODULE', 'IMPORT',
])
