OBERON_BASE_TYPES = {
    'INTEGER':  (4, DW_ATE_signed,        'INTEGER'),
    'SET':      (4, DW_ATE_unsigned,      'SET'),
    'REAL':     (4, DW_ATE_float,         'REAL'),
    'CHAR':     (1, DW_ATE_unsigned_char, 'CHAR'),
    'BOOLEAN':  (1, DW_ATE_unsigned,      'BOOLEAN'),
    'BYTE':     (1, DW_ATE_unsigned,      'BYTE'),
    'PROCEDURE': (4, DW_ATE_unsigned,    'PROCEDURE'),
}
