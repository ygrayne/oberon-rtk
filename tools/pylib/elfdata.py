def compute_leaf_locations(proc_src, type_sizes=None):
    param_locs = {}
    local_locs = {}
    reg_idx = 0
    for param in proc_src.params:
        is_open = param.typeref.open_array
        is_record = _is_record_typeref(param.typeref, type_sizes)
        for name in param.names:
            if is_open:
                param_locs[name] = _loc_reg(reg_idx)
            elif param.by_ref or is_record:
                param_locs[name] = _loc_breg(reg_idx, 0)
            else:
                param_locs[name] = _loc_reg(reg_idx)
            reg_idx += 1
            if is_record:
                reg_idx += 1
        if is_open:
            reg_idx += 1
    for var in proc_src.local_vars:
        is_array = _is_array_typeref(var.typeref)
        is_record = _is_record_typeref(var.typeref, type_sizes)
        is_bool = (var.typeref.name == 'BOOLEAN' and not is_array)
        if not is_bool and not is_array and not is_record:
            for name in var.names:
                local_locs[name] = _loc_reg(reg_idx)
                reg_idx += 1
    stack_offset = 0
    for var in proc_src.local_vars:
        is_array = _is_array_typeref(var.typeref)
        is_record = _is_record_typeref(var.typeref, type_sizes)
        is_bool = (var.typeref.name == 'BOOLEAN' and not is_array)
        if is_bool:
            for name in var.names:
                local_locs[name] = _loc_fbreg(stack_offset)
                stack_offset += 1
        elif is_array or is_record:
            stack_offset = (stack_offset + 3) & ~3
            sz = type_size_ext(var.typeref, type_sizes)
            alloc_sz = (sz + 3) & ~3
            for name in var.names:
                local_locs[name] = _loc_fbreg(stack_offset)
                stack_offset += alloc_sz
    return param_locs, local_locs
