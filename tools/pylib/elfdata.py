def tokenize_alst(filepath):
    raw_lines = filepath.read_text(encoding='utf-8', errors='replace').splitlines()
    chars = []
    for line_no, line in enumerate(raw_lines, 1):
        if line.startswith('.'):
            continue
        for ch in line:
            chars.append((ch, line_no))
        chars.append(('\n', line_no))
    tokens = []
    pos = 0
    n = len(chars)
    while pos < n:
        c, line = chars[pos]
        if c in ' \t\r\n':
            pos += 1
            continue
        if c == '(' and pos + 1 < n and chars[pos + 1][0] == '*':
            pos += 2
            depth = 1
            while pos < n and depth > 0:
                c2 = chars[pos][0]
                if c2 == '(' and pos + 1 < n and chars[pos + 1][0] == '*':
                    depth += 1
                    pos += 2
                elif c2 == '*' and pos + 1 < n and chars[pos + 1][0] == ')':
                    depth -= 1
                    pos += 2
                else:
                    pos += 1
            continue
        if c == '"':
            pos += 1
            while pos < n and chars[pos][0] != '"':
                pos += 1
            if pos < n:
                pos += 1
            continue
        if c.isalpha() or c == '_':
            start = pos
            while pos < n and (chars[pos][0].isalnum() or chars[pos][0] == '_'):
                pos += 1
            text = ''.join(x[0] for x in chars[start:pos])
            tokens.append(Token(TK.IDENT, text, line))
            continue
        if c.isdigit():
            start = pos
            while pos < n and chars[pos][0].isdigit():
                pos += 1
            text = ''.join(x[0] for x in chars[start:pos])
            while pos < n and chars[pos][0].isalnum():
                pos += 1
            tokens.append(Token(TK.INTEGER, text, line))
            continue
        if c == '.' and pos + 1 < n and chars[pos + 1][0] == '.':
            tokens.append(Token(TK.DOTDOT, '..', line))
            pos += 2
            continue
        _singles = {
            ',': TK.COMMA, ':': TK.COLON, ';': TK.SEMI, '.': TK.DOT,
            '*': TK.STAR,  '=': TK.EQ,    '(': TK.LPAREN, ')': TK.RPAREN,
            '[': TK.LBRACKET, ']': TK.RBRACKET,
        }
        if c in _singles:
            tokens.append(Token(_singles[c], c, line))
            pos += 1
            continue
        pos += 1
    last_line = chars[-1][1] if chars else 1
    tokens.append(Token(TK.EOF, '', last_line))
    return tokens
class OberonParser:
    def __init__(self, tokens):
        self.tokens = tokens
        self.pos = 0
        self.const_defs = {}
    def _cur(self):
        return self.tokens[self.pos]
    def _peek(self, offset=1):
        i = min(self.pos + offset, len(self.tokens) - 1)
        return self.tokens[i]
    def _consume(self):
        tok = self.tokens[self.pos]
        if self.pos < len(self.tokens) - 1:
            self.pos += 1
        return tok
    def _is(self, text):
        t = self._cur()
        return t.kind == TK.IDENT and t.text == text
    def _at_section(self):
        t = self._cur()
        return t.kind == TK.IDENT and t.text in _SECTION_KEYWORDS
    def _skip_to_semi(self):
        while self._cur().kind not in (TK.SEMI, TK.EOF):
            self._consume()
        if self._cur().kind == TK.SEMI:
            self._consume()
    def parse_module(self):
        while self._cur().kind != TK.EOF and not self._is('MODULE'):
            self._consume()
        if self._cur().kind == TK.EOF:
            return None
        self._consume()
        name_tok = self._consume()
        module_name = name_tok.text if name_tok.kind == TK.IDENT else 'Unknown'
        if self._cur().kind == TK.SEMI:
            self._consume()
        global_vars = []
        type_defs = {}
        imports = {}
        procedures = []
        record_decl_lines = {}
        while self._cur().kind != TK.EOF:
            if self._is('IMPORT'):
                imports.update(self._parse_imports())
            elif self._is('CONST'):
                self._consume()
                self._parse_const_section()
            elif self._is('TYPE'):
                self._parse_type_decl(type_defs, record_decl_lines)
            elif self._is('VAR'):
                self._parse_var_decl(global_vars)
            elif self._is('PROCEDURE'):
                proc = self._parse_procedure()
                if proc:
                    procedures.append(proc)
            elif self._is('BEGIN') or self._is('END'):
                break
            else:
                self._consume()
        return ModuleSource(
            name=module_name,
            global_vars=global_vars,
            type_defs=type_defs,
            type_sizes={},
            procedures=procedures,
            imports=imports,
            record_decl_lines=record_decl_lines,
        )
    def _parse_imports(self):
        self._consume()
        imports = {}
        while self._cur().kind not in (TK.SEMI, TK.EOF):
            if self._cur().kind == TK.IDENT:
                alias = self._consume().text
                if (self._cur().kind == TK.COLON and
                        self._peek().kind == TK.EQ):
                    self._consume()
                    self._consume()
                    if self._cur().kind == TK.IDENT:
                        module_name = self._consume().text
                        imports[alias] = module_name
                else:
                    imports[alias] = alias
            elif self._cur().kind == TK.COMMA:
                self._consume()
            else:
                self._consume()
        if self._cur().kind == TK.SEMI:
            self._consume()
        return imports
    def _parse_const_section(self):
        while self._cur().kind != TK.EOF and not self._at_section():
            if self._cur().kind != TK.IDENT:
                self._consume()
                continue
            name = self._consume().text
            if self._cur().kind == TK.STAR:
                self._consume()
            if self._cur().kind != TK.EQ:
                while self._cur().kind not in (TK.SEMI, TK.EOF) and not self._at_section():
                    self._consume()
                if self._cur().kind == TK.SEMI:
                    self._consume()
                continue
            self._consume()
            if self._cur().kind == TK.INTEGER:
                val_text = self._consume().text
                if self._cur().kind == TK.SEMI:
                    try:
                        self.const_defs[name] = int(val_text)
                    except ValueError:
                        pass
            while self._cur().kind not in (TK.SEMI, TK.EOF) and not self._at_section():
                self._consume()
            if self._cur().kind == TK.SEMI:
                self._consume()
    def _parse_type_decl(self, type_defs, record_decl_lines=None):
        self._consume()
        while (self._cur().kind == TK.IDENT and
               self._cur().text not in _SECTION_KEYWORDS):
            name_tok = self._consume()
            name = name_tok.text
            name_line = name_tok.line
            if self._cur().kind == TK.STAR:
                self._consume()
            if self._cur().kind != TK.EQ:
                self._skip_to_semi()
                continue
            self._consume()
            typeref = self._parse_type()
            if typeref.name == 'RECORD':
                typeref = TypeRef(name=name, fields=typeref.fields, base=typeref.base)
                _name_anonymous_records(typeref, name)
                if record_decl_lines is not None:
                    record_decl_lines[name] = name_line
            elif typeref.pointer_to is not None:
                typeref = TypeRef(name=name, pointer_to=typeref.pointer_to)
            elif typeref.is_proc_type:
                typeref = TypeRef(name=name, is_proc_type=True)
            type_defs[name] = typeref
            if self._cur().kind == TK.SEMI:
                self._consume()
    def _parse_var_decl(self, var_list):
        self._consume()
        while (self._cur().kind == TK.IDENT and
               self._cur().text not in _SECTION_KEYWORDS):
            decl_line = self._cur().line
            names, exported_names = self._parse_identlist_ex()
            if not names:
                break
            if self._cur().kind != TK.COLON:
                self._skip_to_semi()
                continue
            self._consume()
            typeref = self._parse_type()
            var_list.append(VarInfo(names=names, typeref=typeref, decl_line=decl_line,
                                    exported_names=exported_names))
            if self._cur().kind == TK.SEMI:
                self._consume()
    def _parse_identlist_ex(self):
        names = []
        exported = set()
        if self._cur().kind != TK.IDENT:
            return names, exported
        n = self._consume().text
        names.append(n)
        if self._cur().kind == TK.STAR:
            self._consume()
            exported.add(n)
        while self._cur().kind == TK.COMMA:
            self._consume()
            if self._cur().kind == TK.IDENT:
                n = self._consume().text
                names.append(n)
                if self._cur().kind == TK.STAR:
                    self._consume()
                    exported.add(n)
        return names, exported
    def _parse_identlist(self):
        names, _ = self._parse_identlist_ex()
        return names
    def _parse_type(self):
        tok = self._cur()
        if tok.kind == TK.IDENT:
            if tok.text == 'ARRAY':
                return self._parse_array_type()
            if tok.text == 'RECORD':
                return self._parse_record_type()
            if tok.text == 'POINTER':
                return self._parse_pointer_type()
            if tok.text == 'PROCEDURE':
                return self._parse_procedure_type()
            name = self._consume().text
            if (self._cur().kind == TK.DOT and
                    self._peek().kind == TK.IDENT):
                self._consume()
                qual = self._consume().text
                name = f'{name}.{qual}'
            return TypeRef(name=name)
        return TypeRef(name='UNKNOWN')
    def _try_parse_dim(self):
        if self._cur().kind == TK.INTEGER:
            return int(self._consume().text)
        if (self._cur().kind == TK.IDENT and
                self._cur().text in self.const_defs):
            return self.const_defs[self._consume().text]
        return None
    def _parse_array_type(self):
        self._consume()
        if self._is('OF'):
            self._consume()
            elem = self._parse_type()
            return TypeRef(name=elem.name, open_array=True, elem=elem)
        dims = []
        dim = self._try_parse_dim()
        if dim is not None:
            dims.append(dim)
        while self._cur().kind == TK.COMMA:
            self._consume()
            dim = self._try_parse_dim()
            if dim is not None:
                dims.append(dim)
        while self._cur().kind != TK.EOF and not self._is('OF'):
            self._consume()
        if self._is('OF'):
            self._consume()
        elem = self._parse_type()
        if not dims:
            dims = [0]
        result = elem
        for d in reversed(dims):
            result = TypeRef(name=result.name, fixed_dim=d, elem=result)
        return result
    def _parse_record_type(self):
        self._consume()
        base = None
        if self._cur().kind == TK.LPAREN:
            self._consume()
            if self._cur().kind == TK.IDENT:
                base = self._consume().text
                if (self._cur().kind == TK.DOT and
                        self._peek().kind == TK.IDENT):
                    self._consume()
                    base = f'{base}.{self._consume().text}'
            if self._cur().kind == TK.RPAREN:
                self._consume()
        fields = []
        while self._cur().kind != TK.EOF:
            if self._is('END'):
                self._consume()
                break
            if self._cur().kind == TK.IDENT and self._cur().text not in _SECTION_KEYWORDS:
                decl_line = self._cur().line
                names, exported_names = self._parse_identlist_ex()
                if not names:
                    break
                if self._cur().kind != TK.COLON:
                    self._skip_to_semi()
                    continue
                self._consume()
                typeref = self._parse_type()
                fields.append(VarInfo(names=names, typeref=typeref, decl_line=decl_line,
                                      exported_names=exported_names))
                if self._cur().kind == TK.SEMI:
                    self._consume()
            else:
                self._consume()
        return TypeRef(name='RECORD', fields=fields, base=base)
    def _parse_pointer_type(self):
        self._consume()
        if self._is('TO'):
            self._consume()
        target = self._parse_type()
        return TypeRef(name=f'POINTER TO {target.name}', pointer_to=target)
    def _parse_procedure_type(self):
        self._consume()
        if self._cur().kind == TK.LPAREN:
            depth = 1
            self._consume()
            while self._cur().kind != TK.EOF and depth > 0:
                if self._cur().kind == TK.LPAREN:
                    depth += 1
                elif self._cur().kind == TK.RPAREN:
                    depth -= 1
                self._consume()
        if self._cur().kind == TK.COLON:
            self._consume()
            self._parse_type()
        return TypeRef(name='PROCEDURE', is_proc_type=True)
    def _parse_procedure(self):
        proc_line = self._cur().line
        self._consume()
        kind = 'normal'
        int_num = None
        if self._cur().kind == TK.STAR:
            kind = 'leaf'
            self._consume()
        if self._cur().kind != TK.IDENT:
            return None
        name = self._consume().text
        if self._cur().kind == TK.STAR:
            self._consume()
        if self._cur().kind == TK.LBRACKET:
            self._consume()
            if self._cur().kind == TK.INTEGER:
                int_num = int(self._consume().text)
            if self._cur().kind == TK.RBRACKET:
                self._consume()
            if kind == 'leaf':
                kind = 'leaf_interrupt'
            else:
                kind = 'interrupt'
        params = []
        if self._cur().kind == TK.LPAREN:
            params = self._parse_formal_params()
        result = None
        if self._cur().kind == TK.COLON:
            self._consume()
            result = self._parse_type()
        if self._cur().kind == TK.SEMI:
            self._consume()
        if self._is('CONST'):
            self._consume()
            self._parse_const_section()
        local_type_defs = {}
        if self._is('TYPE'):
            self._parse_type_decl(local_type_defs)
        local_vars = []
        while self._is('VAR'):
            self._parse_var_decl(local_vars)
        while self._is('PROCEDURE'):
            self._parse_procedure()
        self._skip_proc_body(name)
        return ProcSource(
            name=name,
            kind=kind,
            int_num=int_num,
            params=params,
            local_vars=local_vars,
            result=result,
            decl_line=proc_line,
            local_type_defs=local_type_defs or None,
        )
    def _parse_formal_params(self):
        self._consume()
        params = []
        while self._cur().kind not in (TK.RPAREN, TK.EOF):
            section = self._parse_fp_section()
            if section:
                params.append(section)
            if self._cur().kind == TK.SEMI:
                self._consume()
        if self._cur().kind == TK.RPAREN:
            self._consume()
        return params
    def _parse_fp_section(self):
        by_ref = False
        if self._is('VAR'):
            by_ref = True
            self._consume()
        decl_line = self._cur().line
        names = self._parse_identlist()
        if not names:
            return None
        if self._cur().kind != TK.COLON:
            return None
        self._consume()
        open_array = False
        if self._is('ARRAY'):
            self._consume()
            if self._is('OF'):
                self._consume()
                open_array = True
        if self._is('PROCEDURE'):
            typeref = self._parse_procedure_type()
            return ParamInfo(names=names, typeref=typeref, by_ref=by_ref, decl_line=decl_line)
        if self._cur().kind != TK.IDENT:
            return None
        base_name = self._consume().text
        if (self._cur().kind == TK.DOT and
                self._peek().kind == TK.IDENT):
            self._consume()
            base_name = f'{base_name}.{self._consume().text}'
        typeref = TypeRef(name=base_name, open_array=open_array)
        if open_array:
            typeref.elem = TypeRef(name=base_name)
        return ParamInfo(names=names, typeref=typeref, by_ref=by_ref, decl_line=decl_line)
    def _skip_proc_body(self, proc_name):
        tok = self._cur()
        if tok.kind == TK.IDENT and tok.text == 'BEGIN':
            self._consume()
            depth = 1
            while self._cur().kind != TK.EOF and depth > 0:
                t = self._cur()
                if t.kind == TK.IDENT:
                    if t.text in _END_OPEN:
                        depth += 1
                    elif t.text == 'END':
                        depth -= 1
                self._consume()
        elif tok.kind == TK.IDENT and tok.text == 'END':
            self._consume()
        else:
            depth = 0
            while self._cur().kind != TK.EOF:
                t = self._cur()
                if t.kind == TK.IDENT:
                    if t.text in _END_OPEN:
                        depth += 1
                    elif t.text == 'END':
                        if depth == 0:
                            self._consume()
                            if (self._cur().kind == TK.IDENT and
                                    self._cur().text == proc_name):
                                self._consume()
                                if self._cur().kind == TK.SEMI:
                                    self._consume()
                            return
                        else:
                            depth -= 1
                            if depth == 0:
                                self._consume()
                                if (self._cur().kind == TK.IDENT and
                                        self._cur().text == proc_name):
                                    self._consume()
                                    if self._cur().kind == TK.SEMI:
                                        self._consume()
                                    return
                                continue
                self._consume()
            return
        if self._cur().kind == TK.IDENT:
            self._consume()
        if self._cur().kind == TK.SEMI:
            self._consume()
_MAP_MODULE_RE = re.compile(r'^(\w+)\s+([0-9A-Fa-f]+)H\s+(\d+)')
def parse_map_file(map_path):
    result = {}
    for line in Path(map_path).read_text(errors='replace').splitlines():
        m = _MAP_MODULE_RE.match(line.strip())
        if m:
            name = m.group(1)
            addr = int(m.group(2), 16)
            size = int(m.group(3))
            if size > 0 and name not in ('End', 'Total'):
                result[name] = addr
    return result
def compute_global_addresses(var_list, data_addr_top, type_sizes=None):
    result = {}
    ptr = data_addr_top
    for var in var_list:
        sz = type_alloc_size(var.typeref, type_sizes)
        align = type_align_ext(var.typeref, type_sizes)
        for name in var.names:
            ptr = (ptr - sz) & ~(align - 1)
            result[name] = ptr
    return result
def compute_local_addresses(var_list, type_sizes=None):
    result = {}
    ptr = 0
    for var in var_list:
        sz = type_alloc_size(var.typeref, type_sizes)
        align = type_align_ext(var.typeref, type_sizes)
        for name in var.names:
            if align > 1:
                ptr = (ptr + align - 1) & ~(align - 1)
            result[name] = ptr
            ptr += sz
    ptr = (ptr + 3) & ~3
    return result, ptr
def compute_param_offsets(params, local_size, type_sizes=None):
    result = {}
    reg_idx = 0
    for param in params:
        is_open = param.typeref.open_array
        is_record = _is_record_typeref(param.typeref, type_sizes)
        reg_per = 2 if is_open else 1
        for name in param.names:
            offset = local_size + reg_idx * 4
            result[name] = (offset, param.typeref, param.by_ref, is_open, is_record)
            reg_idx += reg_per
            if is_record:
                reg_idx += 1
    return result
